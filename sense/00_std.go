func ToStr[T ISenseCodes](asc string, ascq string) string {
	asc = strings.ToUpper(asc)
	ascq = strings.ToUpper(ascq)

	switch {
	case asc == "40" && ascq != "00":
		return fmt.Sprintf("DIAGNOSTIC FAILURE ON COMPONENT 0x%s", ascq)
	case asc == "4D":
		return fmt.Sprintf("TAGGED OVERLAPPED COMMANDS (0x%s = TASK TAG)", ascq)
	case asc == "70":
		return fmt.Sprintf("DECOMPRESSION EXCEPTION SHORT ALGORITHM ID OF 0x%s", ascq)
	}

	var senseCodes T
	senseCodes.Init()

	x, ok := senseCodes.Get(asc, ascq)
	if !ok {
		x = fmt.Sprintf("Unknown, Obsolete, Reserved or not implemented ASC(%s) and ASCQ(%s) codes", asc, ascq)
	}
	return x
}

type Standard struct{ standardSenseCodes map[string]string }
type ISenseCodes interface {
	Init()
	Get(asc string, ascq string) (string, bool)
}

func (x Standard) Get(asc string, ascq string) (string, bool) {
	code := fmt.Sprintf("%s%s", asc, ascq)
	val, ok := x.standardSenseCodes[code]
	return val, ok
}

